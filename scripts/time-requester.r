view center-face layout/tight [
    across
    g: at image load (to-binary decompress small-picture) feel [
        engage: func [f a e] [
            if a = 'down [
                foreach location (extract hours 2) [
                    if within? e/offset location 20x20 [
                        h/text: form select hours location show h
                        b1/offset: location show b1
                    ]
                ]
                foreach location (extract minutes 2) [
                    if within? e/offset location 20x20 [
                        m/text: form select minutes location show m
                        b2/offset: location show b2
                    ]
                ]
            ]
        ]
    ] 
    return
    text "Hour:" h: field 30 "12"
    text "Minute:" m: field 30 "00"
    p: drop-down 50 data ["am" "pm"]
    btn "Show" [probe to-time rejoin [h/text ":" m/text p/text]]
    return text " "  return text " " ; space for dropdown
    at -100x-100 b1: box red 20x20 "h"
    at -100x-100 b2: box blue 20x20 "m"
]


