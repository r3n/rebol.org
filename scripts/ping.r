ping: does [
	buffer: copy ""
	clear result/text
	show result
	commande: join "ping " ["-" countos " " count/text " " ipadr/text]
	bt/text: join "Ping " ipadr/text
	show  bt
	; call external call
	call/output commande buffer
	bt/text: "Ping " 
	show  bt
	append result/text buffer
	; for Unices OS
	n: to-integer length? buffer
	if n = 0 [append result/text "Network Error"]
	show  result
]



