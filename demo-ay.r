A l'origine était le verbe, puis REBOL est apparu....           hum...             
  ok ! c'est nul ! Faut que j'arrête avec cette vanne.         Bon ! sérieusement, cette demo illustre 
 ma tentative de réaliser un émulateur pour le PSG AY-3-8912 (puce sonore) .        Alors, 
 pourquoi faire me direz vous !????      D'abord un peu d'histoire: Ce PSG équipe de nombreux PC (personnal computer) des années 80.
 On peut citer par exemple le standard MSX ainsi que le ZX-spectrum ou encore l'Amstrad CPC
 qui bien que possédant le même PSG, réussissait le tour de force de produire un son de casserole... Enfin, bref, je m'écarte du sujet.
 Ces merveilleuses machines 8bits ne sont pas mortes, de nombreuses communautés continuent de développer pour et avec ces antiquités.
 On en vient donc à parler de ma passion, jouer et développer sur MSX. Et oui braves gens,
 les jeux sur PC dual core et autres consoles de dernière génération
 n'ont que peu d'intérêt pour moi, je préfère m'éclater sur un bon vieux Konami des familles.
 Accessoirement, je développe en assembleur sur MSX et c'est là que REBOL intervient.
 J'utilise REBOL pour développer des outils d'aide à la conception de programmes en assembleur Z80. 
 Et voilà, vous savez tout.       Maintenant, parlons un peu de cette demo.
 la musique que vous entendez est tirée du jeu "Auf wiedersehen Monty". Elle est complètement synthétisée par Rebol. Je veux dire par là,
 qu'il n'y a pas de fichier wav compressé en entrée mais une série de données qui
 correspondent aux valeurs contenues dans les registres du PSG.
 Ces valeurs se limitent à 3 fréquences + 3 hauteurs de volume, traités
 tout les 50ièmes de seconde par REBOL qui fonctionne donc comme un synthétiseur 3 voies.
 Et le pire, (plutôt le mieux) c'est que REBOL a encore le temps de faire des animations inutiles...
 Bien sûr cette synthèse est loin d'être parfaite, par exemple en lieu et place du son de batterie
 on entend une petite clochette. Et puis, il y a ZE problème: le son est injecté dans le port sound de rebol
 toutes les 5 secondes. Malheureusement à cette occasion, on entend un "blanc" désagréable
 (dont la durée dépend aussi de la rapidité de votre PC) ainsi qu'un léger blocage du script.
 C'est triste parce que à cause de ce bug, le streaming audio temps réel est impossible, ou alors,
 j'ai fait un truc qui fallait pas dans le script, snif !!!!              Pour finir ce long discours,
 je tiens à remercier Goldevil du forum REBOL France pour son aide indispensable dans le décortiquage et la compréhension
 du fonctionnement du PSG.         Vala !!!            A plousss....        Arghhhhh! Put1 de scroll saccadéééééééééééééé !!!!

                                                                
