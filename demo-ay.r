    ; Set up the sound sample parameters:
    sample: make sound [
        rate: 44100 
        channels: 1
        bits: 8
        volume: 1
        data: #{}
    ]
   clock: 3579545 / 32

   cache: make list! 100
    ; get the semi period of a frequency
   make-period: func [freq /local note rep ret pitch phase] [
        if freq = 0 [return cp [0 10000 10000]]
        periode: to integer! sample/rate / freq  / 2 + 1
        reduce [1 periode periode]
    ]

    tone: make binary! len: to-integer sample/rate / inter + 0.5
    draw-oscillo: make block! 4000
    insert/dup draw-oscillo [vline 50 'hline 0] 1000
    cache: make block! 8 * inter
    insert/dup/only cache [move 0x0] 8 * inter + 1
    scroll: 0
    image: make image! as-pair to integer! len / 2 100
    mix: func [/local noise][
        pitch: 1
        tone: clear head tone
        draw-oscillo: next head draw-oscillo
        while [pitch <= len] [
                if toneA/2 = 0 [poke toneA 2 toneA/3 change toneA negate toneA/1]
                if toneB/2 = 0 [poke toneB 2 toneB/3 change toneB negate toneB/1]
                if toneC/2 = 0 [poke toneC 2 toneC/3 change toneC negate toneC/1]
                split: min min toneA/2 toneB/2 toneC/2
                val: to integer! (toneA/1 * volA / 15) + (toneB/1 * volB  / 15) + (toneC/1 * volC / 15) * 25
                val: max -127 min 127 val
                change draw-oscillo val / 2 + 50
                change skip draw-oscillo 2 split / 2
                draw-oscillo: skip draw-oscillo 4
                insert/dup tone to char! val + 128 split
                poke toneA 2 toneA/2 - split
                poke toneB 2 toneB/2 - split
                poke toneC 2 toneC/2 - split
                tone: tail tone
                pitch: pitch + split
        ]
         oscillo: cp/part head draw-oscillo back draw-oscillo
         insert tail oscillo [move 0x0]
         image/rgb: black
         ii/image: draw image pick cache time + 1
         scroll: scroll + 1 // 4
         if scroll = 0 [txt: next txt if tail? txt [txt: head txt]]
         poke cache time + 1  head insert/only tail compose [
                text (as-pair  scroll * -1 0) (cp/part txt 130)
                line-width 4 line-pattern 15 1 
                pen blue black line 0x87 (as-pair to integer! volA * len / 30 87)
                pen yellow black line 0x92 (as-pair to integer! volB * len / 30 92)
                pen green black line 0x97 (as-pair to integer! volB * len / 30 97)
                line-pattern 1
                line-width 2 pen red line 0x50 0x50 shape
         ] oscillo 
         ;stats/text: system/stats
         show ii ;show [stats ii]
         ;recycle
         wait max 0 1 + time / inter + third refresh - now/time/precise
        head tone
    ]

